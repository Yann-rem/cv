export const emailRegex = new RegExp(
  "^(?=[a-z0-9@.!#$%&'*+/=?^_`{|}~-]{6,254}$)" +
    "(?=[a-z0-9@.!#$%&'*+/=?^_`{|}~-]{1,64}@)" +
    "[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@" +
    "(?:(?=[a-z0-9-]{1,63}\\.)[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+" +
    "(?=[a-z0-9-]{1,63}$)[a-z0-9](?:[a-z0-9-]*[a-z0-9])?$",
  "i"
);

export const phoneRegex = new RegExp(
  "^(?:(?:(?:\\+|00)33[ ]?(?:\\(0\\)[ ]?)?)|0){1}[1-9]{1}([ .-]?)(?:\\d{2}\\1?){3}\\d{2}$"
);
